"""사고 입력 충분성 판단에 사용할 프롬프트를 모아둡니다."""


def build_intake_prompt(user_input: str) -> str:
    """사용자 사고 설명을 충분성 판단용 프롬프트로 변환합니다."""
    return f"""
사용자의 자동차 사고 설명을 읽고 과실비율 분석에 충분한지 판단해.

판단 기준:
- 사고 장소 유형
- 각 당사자의 진행 방향
- 신호, 표지, 차선 정보
- 사고 직전 행동
- 충돌 지점이나 접촉 부위
- 과속, 불법주정차, 급정거 같은 특이 요소

모든 정보가 완벽할 필요는 없고, 사고유형 후보를 좁힐 수 있으면 충분하다고 판단해.

[사용자 입력]
{user_input}
""".strip()

